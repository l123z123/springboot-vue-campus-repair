# 1. 获取 Token
$token = $env:MY_JWT_TOKEN

if (-not $token) {
    Write-Host "❌ Error: Token not found in environment variable MY_JWT_TOKEN." -ForegroundColor Red
    Write-Host "💡 Hint: Please run this command first in your terminal:" -ForegroundColor Yellow
    Write-Host "   `$env:MY_JWT_TOKEN = `"eyJhbGciOiJIUzI1NiJ9...`"" -ForegroundColor Gray
    exit 1
}

Write-Host "✅ Info: Token loaded (Length: $($token.Length))" -ForegroundColor Cyan

# 2. 准备测试文件 —— 使用脚本所在目录作为基准
$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$filePath = Join-Path $scriptDir "test_upload.txt"

# 如果文件不存在，自动生成一个
if (!(Test-Path $filePath)) {
    Write-Host "📝 Info: Creating test file at: $filePath" -ForegroundColor Gray
    $content = "This is a test file generated by PowerShell script.`nCreated on: $(Get-Date)`nSystem: Campus Repair System Test."
    $content | Out-File -FilePath $filePath -Encoding UTF8
    Write-Host "   -> File created successfully." -ForegroundColor Green
}

# 3. 构建 Multipart/form-data 请求体
$boundary = [System.Guid]::NewGuid().ToString()
$fileContent = [System.IO.File]::ReadAllBytes($filePath)
$fileName = Split-Path $filePath -Leaf

$bodyBuilder = New-Object System.Text.StringBuilder

# 添加边界
$bodyBuilder.AppendLine("--$boundary") > $null
# 添加文件头信息
$bodyBuilder.AppendLine("Content-Disposition: form-data; name=`"file`"; filename=`"$fileName`"") > $null
$bodyBuilder.AppendLine("Content-Type: application/octet-stream") > $null
$bodyBuilder.AppendLine() > $null
# 添加文件内容 (注意：对于二进制文件此方法可能不完美，但对于 txt 测试文件完全没问题)
$bodyBuilder.Append([System.Text.Encoding]::UTF8.GetString($fileContent)) > $null
$bodyBuilder.AppendLine() > $null
# 添加结束边界
$bodyBuilder.AppendLine("--$boundary--") > $null

$body = $bodyBuilder.ToString()
$contentType = "multipart/form-data; boundary=$boundary"

# 4. 配置请求地址
# 【关键修正】
# Context Path: /api
# Controller Mapping: /file
# Method Mapping: /upload
# 最终 URL: http://localhost:8081/api/file/upload
$uploadUrl = "http://localhost:8081/api/file/upload"
$healthUrl = "http://localhost:8081/api/actuator/health"

Write-Host "🌐 Info: Target Upload URL: $uploadUrl" -ForegroundColor Cyan

# 5. 预检查：后端服务是否存活？
try {
    Invoke-WebRequest -Uri $healthUrl -Method Get -TimeoutSec 3 -ErrorAction Stop | Out-Null
    Write-Host "🟢 Info: Backend service is RUNNING and reachable." -ForegroundColor Green
} catch {
    Write-Host "🟠 Warning: Cannot reach backend at $healthUrl" -ForegroundColor Yellow
    Write-Host "   Is 'mvn spring-boot:run' running in another window?" -ForegroundColor Gray
    Write-Host "   Continuing to try upload anyway..." -ForegroundColor Gray
}

# 6. 设置请求头
$headers = @{ 
    Authorization = "Bearer $token" 
}

# 7. 发送上传请求
Write-Host "⏳ Uploading file '$fileName'..." -ForegroundColor Gray

try {
    $response = Invoke-RestMethod -Uri $uploadUrl -Method Post -Headers $headers -ContentType $contentType -Body $body
    
    Write-Host "🎉 Success: File uploaded!" -ForegroundColor Green
    Write-Host "📄 Server Response:" -ForegroundColor Gray
    # 格式化输出 JSON
    $response | ConvertTo-Json -Depth 10
} 
catch {
    Write-Host "💥 Error: Upload failed!" -ForegroundColor Red
    
    $errorMsg = $_.Exception.Message
    Write-Host "Exception: $errorMsg" -ForegroundColor Yellow
    
    if ($_.ErrorDetails.Message) {
        $serverMsg = $_.ErrorDetails.Message
        Write-Host "Server Detail: $serverMsg" -ForegroundColor DarkYellow
        
        # 智能错误分析
        if ($serverMsg -like "*401*" -or $serverMsg -like "*Unauthorized*" -or $serverMsg -like "*Token*") {
            Write-Host "⚠️  Cause: Token is invalid or expired." -ForegroundColor Magenta
            Write-Host "💡 Solution: Get a NEW token from Postman and re-set the env variable." -ForegroundColor Cyan
        }
        elseif ($serverMsg -like "*404*" -or $serverMsg -like "*Not Found*") {
            Write-Host "⚠️  Cause: Endpoint URL is incorrect (404)." -ForegroundColor Magenta
            Write-Host "💡 Check: Is your FileController mapped to '/file' (not '/api/file')?" -ForegroundColor Cyan
            Write-Host "💡 Current URL used: $uploadUrl" -ForegroundColor Gray
        }
        elseif ($serverMsg -like "*500*") {
            Write-Host "⚠️  Cause: Server internal error." -ForegroundColor Magenta
            Write-Host "💡 Check: Look at the backend console logs for stack trace." -ForegroundColor Cyan
        }
    } else {
        # 如果没有详细错误信息，可能是网络问题或参数格式问题
        Write-Host "💡 Hint: Check if the backend is running and the URL is correct." -ForegroundColor Gray
    }
}